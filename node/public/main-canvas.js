function m_v( u, v){
    var result = [];

    for ( var i = 0; i < 4; ++i ) {
        var sum = 0.0;
        for ( var k = 0; k < 4; ++k ) {
            sum += u[i][k] * v[k];
        }
        result.push( sum );
    }

    return result;

}

function cutten( v){
    let m = 1;
    if (v[3]!=0) m = v[3];
    return vec3(v[0]/m, v[1]/m, v[2]/m);
}


